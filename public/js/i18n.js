const LANG = (() => {
  const stored = localStorage.getItem('nebula_lang');
  if (stored && (stored === 'de' || I18N[stored])) return stored;
  return (navigator.language || 'de').toLowerCase().startsWith('de') ? 'de' : 'en';
})();
const LOCALE = LANG === 'de' ? 'de-DE' : 'en-GB';

function t(s) {
  if (LANG === 'de') return s;
  const d = I18N[LANG];
  return (d && d[s]) || s;
}
function tf(s, ...args) {
  let out = t(s);
  args.forEach((v, i) => { out = out.replace('{' + i + '}', v); });
  return out;
}
function setLang(l) {
  localStorage.setItem('nebula_lang', l);
  location.reload();
}
